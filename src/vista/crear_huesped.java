    private void btm_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btm_crearActionPerformed
        String usuarip=txt_user.getText();
        String nombre=txt_nom.getText();
        String apellido=txt_apell.getText();
        String cont=txt_cont.getText();
        String fecha_nac=txt_fech.getText();
        String nacionalidad=txt_nacio.getText();
        String telefono=txt_telf.getText();
        if(usuarip.equals("") || nombre.equals("") || apellido.equals("") || cont.equals("") || fecha_nac.equals("") || nacionalidad.equals("") || telefono.equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese todos los valores","FALLIDO", JOptionPane.ERROR_MESSAGE);
        }else{
            nuevo_huesped.setUsuario(txt_user.getText());
            nuevo_huesped.setNombre(txt_nom.getText());
            nuevo_huesped.setApellido(txt_apell.getText());
            nuevo_huesped.setCont(txt_cont.getText());
            nuevo_huesped.setFecha_naci(txt_fech.getText());
            nuevo_huesped.setNacionalidad(txt_nacio.getText());
            nuevo_huesped.setTelefono(txt_telf.getText());
            nuevo_huesped.setRol(2);
            hc.crearUsuario(nuevo_huesped);
            dispose();
        }
        principal.getMenu_admin().setEnabled(false);
        principal.getMenu_huesped().setEnabled(true);
    }//GEN-LAST:event_btm_crearActionPerformed

