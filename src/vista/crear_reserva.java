    private void bot_reservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_reservaActionPerformed

        String fe=txt_fecha_entrada.getText();
        String es=txt_fecha_salida.getText();
        String val=txt_valor.getText();
        String formp=(String)cmb_forma_pago.getSelectedItem();
        String total=txt_total.getText();
        if(fe.equals("") || es.equals("") || val.equals("") || formp.equals("") || total.equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese todos los valores","FALLIDO", JOptionPane.ERROR_MESSAGE);
        }else{
            nueva_reserva.setFecha_entr(txt_fecha_entrada.getText());
            nueva_reserva.setFecha_sal(txt_fecha_salida.getText());
            nueva_reserva.setValor(Integer.parseInt(txt_valor.getText()));
            nueva_reserva.setForma_pag( (String)cmb_forma_pago.getSelectedItem());
            nueva_reserva.setHuesped(valorGlobal);
            nueva_reserva.setTotal(Double.parseDouble(txt_total.getText()));
            hc.insertarReserva(nueva_reserva);
            dispose();
        }
        principal.getMenu_admin().setEnabled(false);
        principal.getMenu_huesped().setEnabled(true);
    }//GEN-LAST:event_bot_reservaActionPerformed

