    private void cmb_forma_pagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmb_forma_pagoActionPerformed
        //DecimalFormat formato = new DecimalFormat("#.##");
        String selectedItem = (String) cmb_forma_pago.getSelectedItem();

        if (selectedItem.equals("Dolar")) {
            int value = (int) spin_num.getValue();
            double result = value * 20;
            txt_total.setText(String.valueOf(result));
        } else if (selectedItem.equals("Euro")) {
            int value = (int) spin_num.getValue();
            double result = value * 20 * 0.93;
            txt_total.setText(String.valueOf(result));
        } else if (selectedItem.equals("libra")) {
            int value = (int) spin_num.getValue();
            double result = value * 20 * 0.81;
            txt_total.setText(String.valueOf(result));
        }else if (selectedItem.equals("Yen")) {
            int value = (int) spin_num.getValue();
            double result = value * 20 * 140.66;
            txt_total.setText(String.valueOf(result));
        }else if (selectedItem.equals("won")) {
            int value = (int) spin_num.getValue();
            double result = value * 20 * 1323.43;
            txt_total.setText(String.valueOf(result));
        }
    }//GEN-LAST:event_cmb_forma_pagoActionPerformed

