    private void bot_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_eliminarActionPerformed
        int fila = tbl_datos.getSelectedRow();
        DefaultTableModel modelo = (DefaultTableModel) tbl_datos.getModel();
        int id;
        id = Integer.parseInt(tbl_datos.getValueAt(fila, 0).toString());
        try {
            ps = con.prepareStatement("DELETE FROM huesped WHERE id_huesped = " + id);
            int elim = ps.executeUpdate();
            if (elim > 0) {
                JOptionPane.showMessageDialog(null, "Dato eliminado");
                modelo.removeRow(fila);
            } else {
                JOptionPane.showMessageDialog(null, "no selecciono ninguna fila");
            }
        } catch (SQLException ex) {
            Logger.getLogger(control_reserva.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bot_eliminarActionPerformed

    private void bot_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_actualizarActionPerformed

        if (txt_usuario.getText().equals("") || txt_nombre.getText().equalsIgnoreCase("") || txt_apellido.getText().equalsIgnoreCase("") || txt_clave.getText().equalsIgnoreCase("") || txt_fecha_nac.getText().equalsIgnoreCase("") || txt_nacional.getText().equalsIgnoreCase("") || txt_telf.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Ingrese todos los datos");
        } else {
            try {
                ps = con.prepareStatement("UPDATE huesped SET nom_usuario = ?, nombre = ?, apellido = ?, clave = ?, fecha_nac = ?, nacionalidad = ?, telefono = ?, id_rol = ?  WHERE id_huesped = " + txt_id_huesped.getText());
                ps.setString(1, txt_usuario.getText());
                ps.setString(2, txt_nombre.getText());
                ps.setString(3, txt_apellido.getText());
                ps.setString(4, txt_clave.getText());
                ps.setString(5, txt_fecha_nac.getText());
                ps.setString(6, txt_nacional.getText());
                ps.setString(7, txt_telf.getText());
                ps.setString(8, (String) cmb_rol.getSelectedItem());
                int res = ps.executeUpdate();
                if (res > 0) {
                    JOptionPane.showMessageDialog(null, "Datos actualizados correctamente");
                } else {
                    JOptionPane.showMessageDialog(null, "Error al actualizar");
                }
            } catch (SQLException ex) {
                System.out.println("error: " + ex);
            }
        }
        DefaultTableModel modelo = (DefaultTableModel) tbl_datos.getModel();
        modelo.setRowCount(0);
        mostrar(txt_buscar.getText());
        txt_id_huesped.setText("");
        txt_usuario.setText("");
        txt_nombre.setText("");
        txt_apellido.setText("");
        txt_clave.setText("");
        txt_fecha_nac.setText("");
        txt_nacional.setText("");
        txt_telf.setText("");
    }//GEN-LAST:event_bot_actualizarActionPerformed

