    public void mostrar(String valor) {
        DefaultTableModel modelo = (DefaultTableModel) tbl_datos.getModel();

        String sql = "";
        if (valor.equals("")) {
            sql = "SELECT id_huesped, nom_usuario, nombre, apellido, clave, fecha_nac, nacionalidad, telefono, id_rol FROM  huesped";
        } else {
            sql = "SELECT id_huesped, nom_usuario, nombre, apellido, clave, fecha_nac, nacionalidad, telefono, id_rol FROM huesped WHERE nom_usuario LIKE '%" + valor + "%'";
        }
        Object[] fila = new Object[9];
        try {
            Statement st = con.createStatement();
            rs = st.executeQuery(sql);

            while (rs.next()) {
                fila[0] = rs.getString(1);
                fila[1] = rs.getString(2);
                fila[2] = rs.getString(3);
                fila[3] = rs.getString(4);
                fila[4] = rs.getString(5);
                fila[5] = rs.getString(6);
                fila[6] = rs.getString(7);
                fila[7] = rs.getString(8);
                fila[8] = rs.getString(9);
                modelo.addRow(fila);
            }
        } catch (SQLException ex) {
            Logger.getLogger(consulta_huespedes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

